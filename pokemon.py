class Jogador:
    def __init__(self):
        self.moedas=20
        self.inventario=['Poção pequena de cura'] ###append
    def loja(self):
        compra=int(input(f'Digite o número correspondente ao item que deseja comprar!(caso queira voltar, digite 0)\nLoja:\n1){itens[0][0]}: {itens[0][1]} moedas\n2){itens[1][0]}: {itens[1][1]} moedas\n3){itens[2][0]}: {itens[2][1]} moedas\n'))
        if compra ==0:
            return
        compra=itens[compra-1][0]
        for i in range(len(itens)):
            if itens[i][0] == compra:
                if  self.moedas < itens[i][1]:
                    print(f'Você não tem moedas suficientes para comprar esse item!')
                    return
                self.inventario.append(itens[i][0])
                self.moedas+= -(itens[i][1])
                print(self.moedas,self.inventario)
    def usar_itens(self):
        for i in range(len(self.inventario)):
            if i==0:
                print(f'Escolha o número do item que você quer usar:\n')
                print(f'{i+1}) {self.inventario[i]}')
            else:
                print(f'{i+1}) {self.inventario[i]}')
            if i==(len(self.inventario)-1):
                escolher_item=int(input(f'Escolha o número do item que você quer usar:\n'))
                item_para_usar=self.inventario[escolher_item-1]
                for i in range(len(itens)):
                    if itens[i][0]==item_para_usar:
                        retornar= itens[i][1]
                        self.inventario.pop(i-1)
                        return retornar
            
    def moedas_vitoria(self,nivel_inimigo):
        self.moedas+= round(nivel_inimigo*5)